def clean_document(text):
    lines = text.splitlines()
    clean_lines = []

    for line in lines:
        line = line.strip()
        if not line:
            continue

        # remove notebook / JSON noise
        if (
            line.startswith("{") or
            line.startswith("}") or
            line.startswith("\"") or
            "nbformat" in line or
            "cell_type" in line or
            "execution_count" in line
        ):
            continue

        clean_lines.append(line)

    return " ".join(clean_lines)


