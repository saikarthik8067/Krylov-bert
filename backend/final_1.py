def krylov_encode_sentences(sentences, tokenizer, model, max_len=128):
    embeddings = []

    for s in sentences:
        enc = tokenizer(
            s,
            return_tensors="pt",
            truncation=True,
            padding="max_length",
            max_length=max_len
        )

        input_ids = enc["input_ids"].to(device)
        attention_mask = enc["attention_mask"].to(device)

        with torch.no_grad():
            out = model(
                input_ids=input_ids,
                attention_mask=attention_mask
            )

        # CLS-like token (first token)
        emb = out[:, 0, :]
