def bert_encode_sentences(sentences, tokenizer, bert, max_len=128):
    embeddings = []

    for s in sentences:
        enc = tokenizer(
            s,
            return_tensors="pt",
            truncation=True,
            padding="max_length",
            max_length=max_len
        )

        input_ids = enc["input_ids"].to(device)
        attention_mask = enc["attention_mask"].to(device)

        with torch.no_grad():
            out = bert(
                input_ids=input_ids,
                attention_mask=attention_mask
            )

        # CLS token
        emb = out.last_hidden_state[:, 0, :]
        embeddings.append(emb.squeeze(0))

    return torch.stack(embeddings)

