def extract_summary(sentences, embeddings, top_k=5):
    doc_emb = embeddings.mean(dim=0, keepdim=True)
    scores = F.cosine_similarity(embeddings, doc_emb)
    idx = torch.topk(scores, top_k).indices.tolist()
    idx = sorted(idx)
    return [sentences[i] for i in idx]

